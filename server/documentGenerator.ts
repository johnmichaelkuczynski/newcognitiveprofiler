        doc.font('Helvetica')
           .text(psychAnalysis.interpersonalDynamics.detailedAnalysis, {
             align: 'justify',
             paragraphGap: 5
           });
        
        doc.moveDown();
        
        // Add a new page if needed
        if (doc.y > 650) {
          doc.addPage();
        }
        
