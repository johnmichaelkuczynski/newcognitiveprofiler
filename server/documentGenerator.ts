/**
 * Helper to get a friendly provider name
 */
function getProviderName(provider: ModelProvider): string {
  switch (provider) {
    case 'openai':
      return 'OpenAI';
    case 'anthropic':
      return 'Claude';
    case 'perplexity':
      return 'Perplexity';
    default:
      return provider;
  }
}